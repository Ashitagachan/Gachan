class DDS
{
public:
    class LEVEL
    {
    public:
        UChar* ptr;
        UInt size;
        UInt pitch;
    };

    UChar* data;
    UInt datalength;

    DXGI_FORMAT dxgiformat;
    UInt width;
    UInt height;
    UInt bits;
    UInt arraysize;//cubemap == 6(not supported now), other == 1
    UInt mipcount;
	bool cubemap;
    
    bool SetBuffer(UChar* buf, UInt bufsize);
    bool GetLevels(DDS::LEVEL* levels);
    
    typedef UInt (*GETBYTES)(UInt w, UInt h);
    GETBYTES GetBytes;
    

    //以下DXT1-5デコード用
    
    UInt GetDecodeBC()
    {
        if (dxgiformat == DXGI_FORMAT_BC1_TYPELESS  ||
            dxgiformat == DXGI_FORMAT_BC1_UNORM     ||
            dxgiformat == DXGI_FORMAT_BC1_UNORM_SRGB) {
            return 1;
        }
        if (dxgiformat == DXGI_FORMAT_BC2_TYPELESS  ||
            dxgiformat == DXGI_FORMAT_BC2_UNORM     ||
            dxgiformat == DXGI_FORMAT_BC2_UNORM_SRGB) {
            return 2;
        }
        if (dxgiformat == DXGI_FORMAT_BC3_TYPELESS  ||
            dxgiformat == DXGI_FORMAT_BC3_UNORM     ||
            dxgiformat == DXGI_FORMAT_BC3_UNORM_SRGB) {
            return 3;
        }
        return 0;
    }
    UInt GetDecodeBCSize()
    {
        UInt32 levelWidth  = width;
        UInt32 levelHeight = height;
        UInt size = 0;

        //for (int array = 0; array < dds.arraysize; array++) {//cubemap/depth非対応
        for (int mip = 0; mip < (int)mipcount; mip++) {
            size += levelWidth * levelHeight * 4;
            
            levelWidth  = MAX(levelWidth  / 2, 1);
            levelHeight = MAX(levelHeight / 2, 1);
        }
        //}
        return size;
    }
    void DecodeBC(UChar* buf, UInt buflen);
    
};


